/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
    BaseType_t higher_priority_task_woken = pdFALSE;

    if(__HAL_UART_GET_FLAG(&huart2, UART_FLAG_IDLE) == SET) {
        __HAL_UART_CLEAR_IDLEFLAG(&huart2);
        cyp_cmd_tail = cyp_cmd_head;
        cyp_cmd_head
            = huart2.RxXferSize - (uint16_t)huart2.hdmarx->Instance->NDTR;

        if(cyp_cmd_head || cyp_cmd_tail) {
            cli_process_data(cyp_cmd_head, cyp_cmd_tail);
        }
    }
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */
  xTaskNotifyFromISR(
      CLIHandle, 0, eNoAction, &higher_priority_task_woken);
  if(higher_priority_task_woken) {
      taskYIELD();
  }
  /* USER CODE END USART2_IRQn 1 */
}

